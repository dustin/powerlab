randBytes :: Int -> Gen B.ByteString
randBytes n = B.pack `fmap` vectorOf n (choose (0, 255))

genCrcData :: Int -> IO ()
genCrcData n =
  let r = mapM (const (generate arbitrary :: IO B.ByteString)) [1..n] in
    do
      b <- fmap (map $ show . B.unpack) r
      putStrLn $ intercalate ",\n" b

